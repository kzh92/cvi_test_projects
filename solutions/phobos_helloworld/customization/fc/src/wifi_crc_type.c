/*******************************************************************************
* Function Name  :  CRC32_Update
* Description    :  CRC32_Update   //更新参与计算的数据  适用于大数据大文件分包计算整个文件的crc32
* Input          :  uint8_t *pData, uint32_t Length
* Output         :  crc32
* Author         :  mao
* Version        ： V1.0
* History        :  20190910
* Return         :  NONE
* Remark         :
*******************************************************************************/
uint32_t CRC32_Update(CRC32_CTX *ctx, uint8_t *pBuf, uint32_t Len)
{
	uint32_t nReg ; //CRC寄存器
	uint32_t nTemp = 0;
	uint32_t i, n;

	nReg = ctx->crc; //

	for (n = 0; n < Len; n++) {

		nReg ^= (uint32_t) pBuf[ n ];

		for (i = 0; i < 4; i++) {
			nTemp = Crc32Table[(uint8_t)((nReg >> 24) & 0xff)];  //取一个字节，查表
			nReg <<= 8; //丢掉计算过的头一个BYTE
			nReg ^= nTemp; //与前一个BYTE的计算结果异或
		}
	}
	ctx->crc = nReg;
	return nReg;
}

/*******************************************************************************
* Function Name  :  CRC16
* Description    :
* Input          :  void
* Output         :  None
* Author         :  mao
* Version        ： V1.0
* History        :  20190910
* Return         :
* Remark         :
*******************************************************************************/
uint32_t CRC16(uint8_t *puchMsg, uint32_t usDataLen)
{
	unsigned char uchCRCHi = 0xff;		//high byte of CRC initialized
	unsigned char uchCRCLo = 0xff;		//low byte of CRC initialized
	unsigned char uchIndex;

	while (usDataLen--) {
		uchIndex = uchCRCHi ^ *puchMsg++;
		uchCRCHi = uchCRCLo ^ auchCRCHi[uchIndex];
		uchCRCLo = auchCRCLo[uchIndex];
	}
	return (uchCRCHi << 8 | uchCRCLo);
}

