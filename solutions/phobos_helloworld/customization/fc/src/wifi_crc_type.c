/*******************************************************************************
* Function Name  :  CRC32Software
* Description    :  CRC32Software   //查表法计算CRC32 一整包数据使用
* Input          :  uint8_t *pData, uint32_t Length
* Output         :  crc32
* Author         :  mao
* Version        ： V1.0
* History        :  20190910
* Return         :  NONE
* Remark         :
*******************************************************************************/
uint32_t CRC32Software(uint8_t *pData, uint32_t Length)
{
	uint32_t nReg ; //CRC寄存器
	uint32_t nTemp = 0;
	uint32_t i, n;

	nReg = 0xFFFFFFFF; //

	for (n = 0; n < Length; n++) {
		nReg ^= (uint32_t) pData[ n ];

		for (i = 0; i < 4; i++) {
			nTemp = Crc32Table[(uint8_t)((nReg >> 24) & 0xff)];  //取一个字节，查表
			nReg <<= 8; //丢掉计算过的头一个BYTE
			nReg ^= nTemp; //与前一个BYTE的计算结果异或
		}
	}
	return nReg;
}


