uint8_t CRC8(const uint8_t *ptr, uint8_t len)
{
	uint8_t i;
	uint8_t crc = 0x00; /*计算初始的CRC*/
	if (NULL == ptr || 0 == len) {
		return 0;
	}
	while (len--) {
		crc ^= *ptr++;  /* 每次先与计算的数据异或，计算完指向下一数据 */
		for (i = 8; i > 0; --i) { /*下面这段计算过程与计算一个字节crc*/
			if (crc & 0x80)
				crc = (crc << 1) ^ 0x31;
			else
				crc = (crc << 1);
		}
	}
	return (uint8_t)crc;
}

static void invert_uint8(unsigned char *dest, unsigned char *src)
{
	unsigned char i;
	unsigned char tmp;

	tmp = 0;
	for (i = 0; i < 8; i++) {
		if (src[0] & (1 << i)) {
			tmp |= 1 << (7 - i);
		}
	}
	dest[0] = tmp;
}

static void invert_uint16(unsigned short *dest, unsigned short *src)
{
	unsigned char  i;
	unsigned short tmp;

	tmp = 0;
	for (i = 0; i < 16; i++) {
		if (src[0] & (1 << i)) {
			tmp |= 1 << (15 - i);
		}
	}
	dest[0] = tmp;
}

unsigned short CRC16_CCITT(unsigned char *data, unsigned long len)
{
	unsigned char  i;
	unsigned short wCRCin = 0x0000;
	unsigned short wCPoly = 0x1021;
	unsigned char  wChar  = 0;

	while (len--) {
		wChar = *(data++);

		invert_uint8(&wChar, &wChar);

		wCRCin ^= (wChar << 8);
		for (i = 0; i < 8; i++) {
			if (wCRCin & 0x8000) {
				wCRCin = (wCRCin << 1) ^ wCPoly;
			} else {
				wCRCin = wCRCin << 1;
			}
		}
	}

	invert_uint16(&wCRCin, &wCRCin);

	return (wCRCin);
}




